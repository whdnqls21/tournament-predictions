-- 마이그레이션 0001 — 경기별 시작 시간(starts_at) 추가
-- ────────────────────────────────────────────────────────────────────────
-- 이미 배포된 DB(매치 테이블이 존재하는 환경)에 적용한다.
-- 실행: Supabase 대시보드 → SQL Editor → 이 파일 전체 붙여넣기 → Run.
-- 재실행해도 안전하다 (if not exists).
--
-- 의미: 경기마다 시작 시간을 지정할 수 있고, 그 시각이 지나면 해당 경기의
--       예측이 자동으로 마감된다(서버에서 is_locked OR now()>=starts_at 로 판정).
-- ════════════════════════════════════════════════════════════════════════

alter table public.matches
  add column if not exists starts_at timestamptz;
